public class TiposPrimitivos {
    public static void main(String[] args) {
        //TIPO PRIMITIVO É UM DADO NÃO ORIENTADO A OBJETO (não são classes)

        //ocupam o msm espaço de Integer, porém não é primitvo então não possui métodos
        int a1 = 10;

        //Integer é primitivo, então possui alguns métodos
        Integer a2 = 10;
        //um método pode ser transformar em String o dado
        a2.toString();
    }
}
